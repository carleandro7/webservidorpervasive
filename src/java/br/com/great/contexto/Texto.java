package br.com.great.contexto;

public class Texto extends Objeto {

    private String texto;
    private int texto_id, mecsimples_id;

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public int getTexto_id() {
        return texto_id;
    }

    public void setTexto_id(int texto_id) {
        this.texto_id = texto_id;
    }

    public int getMecsimples_id() {
        return mecsimples_id;
    }

    public void setMecsimples_id(int mecsimples_id) {
        this.mecsimples_id = mecsimples_id;
    }


}
